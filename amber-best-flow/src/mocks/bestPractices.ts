// Helper function to convert BestPractice to BestPracticeListItem
export function toListItem(practice: BestPractice): BestPracticeListItem {
  return {
    id: practice.id,
    title: practice.title,
    description: practice.description,
    category_id: practice.category_id,
    category_name: practice.category_name || 'Unknown',
    plant_id: practice.plant_id,
    plant_name: practice.plant_name || 'Unknown',
    submitted_by_name: practice.submitted_by_name || 'Unknown',
    submitted_date: practice.submitted_date,
    status: practice.status,
    savings_amount: practice.savings_amount,
    savings_currency: practice.savings_currency,
    is_benchmarked: practice.is_benchmarked || false,
    question_count: practice.question_count || 0,
    has_images: (practice.images && practice.images.length > 0) || false,
    created_at: practice.created_at,
  };
}


